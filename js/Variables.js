// JavaScript Variables can be declared in 4 ways:

// Automatically
// Using var
// Using let
// Using const

// var ->  functional scope -> can reDeclare -> can reassign -> can declare without initialization
// let ->  block scope -> can reDeclare -> can reassign -> can declare without initialization
// const ->block scope -> can't reDeclare -> can't reassign -> can't declare without initialization
